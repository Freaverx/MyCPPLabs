// Класс для работы со строками (ограниченный буфером 80 символов)
class MyString
{
    char str[80] = "";  // Внутренний буфер для хранения строки (инициализирован пустой строкой)
                         // Опасность: возможное переполнение буфера!
                         // Рекомендация: использовать динамическое выделение памяти

public:
    // Конструкторы:
    MyString(const char*);       // Конструктор из C-строки
    MyString();                  // Конструктор по умолчанию (создает пустую строку)
    MyString(const MyString&);   // Конструктор копирования

    // Перегруженные операторы:
    MyString& operator=(const MyString&);  // Оператор присваивания
    MyString& operator+(const MyString&);  // Оператор конкатенации (нестандартная реализация!)
    int operator==(const MyString&);       // Оператор сравнения (лучше возвращать bool)

    // Методы для работы со строкой:
    int strLen();       // Получение длины строки
    void input();       // Ввод строки (опасность переполнения буфера!)
    void print();       // Вывод строки
};