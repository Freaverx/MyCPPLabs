#include <iostream>
#include "MyString.h"  // Подключение пользовательского заголовочного файла для работы со строками

int main()
{
    setlocale(LC_ALL, "rus");  // Установка русской локали для корректного вывода кириллицы
    
    // Создание нескольких объектов MyString разными способами:
    MyString s1("String #1"),  // Конструктор с параметром (из C-строки)
             s2,               // Конструктор по умолчанию (пустая строка)
             s3(s1),           // Конструктор копирования (из s1)
             s4;               // Еще одна пустая строка

    s2.input();                // Ввод строки s2 с клавиатуры
    s2 = "String #2";          // Присваивание C-строки (использует перегруженный оператор=)
    s2.print();                // Вывод строки s2 на экран

    s4 = s1 + s2 + s3;         // Конкатенация строк (использует перегруженные операторы + и =)
    
    // Вывод длины строки s4 (использует метод strLen())
    std::cout << "Длина s4 = " << s4.strLen() << std::endl;
    s4.print();                // Вывод строки s4 на экран

    // Сравнение строк (использует перегруженный оператор ==)
    if (s1 == s4)
        std::cout << "Строки s1 и s4 равны";
    else if (s1 == s3)
        std::cout << "Строки s1 и s3 равны";  // Этот вариант будет выполнен, так как s3 - копия s1
}